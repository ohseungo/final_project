package com.scsa.model.vo;

public class Disposable {
	private String dispId;
	private String dispType;
	private int dispValue;
	
	private int dispCollected;
	
	private String compId;
	private String compName;
	public String getDispId() {
		return dispId;
	}
	public void setDispId(String dispId) {
		this.dispId = dispId;
	}
	public String getDispType() {
		return dispType;
	}
	public void setDispType(String dispType) {
		this.dispType = dispType;
	}
	public int getDispValue() {
		return dispValue;
	}
	public void setDispValue(int dispValue) {
		this.dispValue = dispValue;
	}
	public int getDispCollected() {
		return dispCollected;
	}
	public void setDispCollected(int dispCollected) {
		this.dispCollected = dispCollected;
	}
	public String getCompId() {
		return compId;
	}
	public void setCompId(String compId) {
		this.compId = compId;
	}
	public String getCompName() {
		return compName;
	}
	public void setCompName(String compName) {
		this.compName = compName;
	}
	
	
}
